/* 
1. Create a function called functionWithArgs that accepts two arguments and outputs their sum to the dev console.
2. Call the function with two numbers as arguments.
*/

// Here we create a function called 'functionWithArgs' that takes two different values. We use 'a' and 'b' as placeholders so that the function expects two different vales.
function functionWithArgs(a, b) { 
    console.log(a + b); // when the function is called the two values passed into the parentheses will be added together and the result will be displayed in the console.
}

functionWithArgs(2, 2); // Here the function is called and passed in 2 and 2. If this is entered then '4' will be printed in the console as 2 + 2 = 4.