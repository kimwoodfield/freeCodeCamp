// Use a for loop to work to push the values 1 through 5 onto myArray.

// Setup
var myArray = []; // variable created called 'myArray' which stores an empty array.

// Only change code below this line


for (var i = 1; i <= 5; i++) { 
    // for loop created.
    // inside the loop, variable created called 'i', 'i' is equal to 1.
    // while i (1) is less than or equal to 5, push the value of (i) onto the array.
    // after the value is pushed onto the array, increase the value of 'i' by 1 and the loop will check the value of i again
    // since 'i' has completed a loop and been incremented, i now equals 2 and since 2 is still less than 6, the loop repeats and adds another value to the array.

    myArray.push(i);

}